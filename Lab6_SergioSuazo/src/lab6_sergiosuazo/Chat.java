    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        try {
            adminUsuario au = new adminUsuario("./Users.txt");
            au.cargarArchivo();
            Date fecha=dc_fecha.getDate();
            Date hoy=new Date();
            String date;
            date= fecha.toString();
            boolean repeat=false;
            for (int i = 0; i < au.getListaPersonas().size(); i++) {
                if(au.getListaPersonas().get(i).getUser().equals(tf_newUser.getText()))
                {
                    repeat=true;
                    break;
                }
            }
            
            if(hoy.getYear()-fecha.getYear()>=13&&!repeat)
            {
                au.getListaPersonas().add(new Usuario(tf_nombre.getText(), tf_newUser.getText(), tf_contra.getText(), date));
                au.escribirArchivo();
            }
            else
            {
                if(hoy.getYear()-fecha.getYear()<13)
                {
                    JOptionPane.showMessageDialog(this, "No tiene la edad para crear una cuenta");
                }
                else if(repeat)
                {
                    JOptionPane.showMessageDialog(this, "Ya existe ese usuario");
                }
                
            }
            JOptionPane.showMessageDialog(this, "Se creo el usuario exitosamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error al crear el ususario");
        }
    }//GEN-LAST:event_jButton3MouseClicked

